    <h3>Beef Tenderloin Black Pepper Sauce</h3>
    <center><img src="image/Picture2.jpg" alt="Gambar Makanan" class="img-radius"></center>
    <p><b>Bahan - bahan :</b></p>
    <ul>
      <li>Daging steak tenderloin (daging has dalam) @200 gram - 2 potong</li>
      <hr>
      <li>Tisu dapur - secukupnya</li>
      <hr>
    </ul>

    <p><b>Bumbu Marinasi :</b></p>
    <ul>
      <li>Garam - 1/2 sdt</li>
      <hr>
      <li>Olive oil / butter / margarin - 1 sdm</li>
      <hr>
      <li>Lada hitam bubuk - 1/2 sdt</li>
      <hr>
      <li>Bawang putih geprek - 2 siung</li>
      <hr>
      <li>Saus tiram - 1 sdm</li>
      <hr>
      <li>Kecap manis - 1 sdm</li>
      <hr>
    </ul>

    <p><b>Saus Black Pepper :</b></p>
    <ul>
      <li>Bawang bombay, cincang kasar - 1/2 siung</li>
      <hr>
      <li>Bawang putih, cincang halus - 2 siung</li>
      <hr>
      <li>Lada hitam bubuk kasar - 1/2 sdt</li>
      <hr>
      <li>Saus lada hitam (1 sachet) - 3 sdm</li>
      <hr>
      <li>Saus tiram - 1 sdm</li>
      <hr>
      <li>Kecap manis - 1 sdt</li>
      <hr>
      <li>Air - 50 ml</li>
      <hr>
      <li>Gula - secukupnya</li>
      <hr>
      <li>Garam - secukupnya</li>
      <hr>
      <li>Kaldu bubuk - secukupnya</li>
      <hr>
      <li>Larutan maizena (opsional) - 1 sdm</li>
      <hr>
    </ul>

    <p><b>Pelengkap :</b></p>
    <ul>
      <li>Kentang goreng - secukupnya</li>
      <hr>
      <li>Mix vegetable - secukupnya</li>
      <hr>
    </ul>
    
    <p>Langkah-langkah:</p>
    <ul>
      <li>Siapkan daging, jika dagingnya berair lap terlebih dahulu menggunakan tisu dapur hingga kering.</li>
      <hr>
      <li>Olesi setiap permukaan daging dengan bumbu marinasi, diamkan selama 2-4 jam di kulkas agar bumbunya lebih meresap.</li>
      <hr>
      <li>Black Pepper Sauce: Tumis bawang putih dan bawang bombay hingga harum, setelah itu masukkan semua bumbu saosnya, koreksi rasa. 
        Jika kurang kental bisa ditambahkan larutan maizena. Saus siap digunakan.</li>
      <hr>
      <li>Tenderloin Steak: Panaskan grill pan/teflon yang telah diberi butter, lalu masak masing-masing sisi 3 menit untuk tingkat kematangan medium 
        (tergantung tingkat ketebalan daging dan kematangan yang diinginkan) kemudian dibalik dan masak sisi lainnya dengan waktu yang sama. 
        Angkat daging dan istirahatkan (resting) terlebih dahulu sekitar 5 menit agar daging tetap juicy, empuk, dan tidak kering.</li>
      <hr>
      <li>Tata kentang goreng dan mix vegetable di piring, sajikan daging steak dengan siraman saus blackpeppernya.</li>
      <hr>
    </ul>
