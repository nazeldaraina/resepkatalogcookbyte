    <h3>Chicken Cordon Bleu</h3>
    <br>
    <center><img src="image/Picture1.jpg" alt="Gambar Makanan" class="img-radius"></center>

    <p><b>Bahan - bahan :</b></p>
    <ul>
      <li>Dada ayam fillet - 2 potong</li>
      <hr>
      <li>Smoked beef - 4 lembar</li>
      <hr>
      <li>Keju mozarella - 2 potong</li>
      <hr>
      <li>Telur, kocok lepas - 1 butir</li>
      <hr>
      <li>Tepung terigu - 5 sdm</li>
      <hr>
      <li>Garam - secukupnya</li>
      <hr>
      <li>Merica bubuk - 1/2 sdt</li>
      <hr>
      <li>Bawang putih bubuk - 1/2 sdt</li>
      <hr>
      <li>Tepung roti - 5 sdm</li>
      <hr>
      <li>Minyak goreng - secukupnya</li>
      <hr>
    </ul>

    <p><b>Pelengkap :</b></p>
    <ul>
      <li>Kentang goreng - 200 gram</li>
      <hr>
      <li>Buncis, rebus - secukupnya</li>
      <hr>
      <li>Wortel, rebus - secukupnya</li>
      <hr>
      <li>Jagung, rebus - secukupnya</li>
      <hr>
      <li>Mayonnaise - secukupnya</li>
      <hr>
      <li>Saus barbecue - secukupnya</li>
      <hr>
    </ul>

    <p><b>Langkah - langkah :</b></p>
    <ul>
      <li>Potong tipis dada ayam fillet tanpa putus, kemudian bentangkan di atas plastik wrap. Pukul-pukul fillet daging sampai tipis dan rata. 
        Taburi dengan garam, merica, dan bawang putih bubuk secukupnya.</li>
      <hr>
      <li>Tata smoked beef dan keju di atasnya, gulung perlahan sambil ditekan dengan bantuan plastik wrap (seperti menggulung sushi), 
        bungkus rapat dengan plastik wrap, simpan di freezer 15 menit sambil menyiapkan bahan pelengkapnya.</li>
      <hr>
      <li>Siapkan terigu, beri sedikit garam dan merica bubuk. Buka plastik wrap, gulingkan gulungan ayam di tepung terigu, 
        celupkan ke dalam kocokan telur, kemudian gulingkan ke tepung roti sampai tertutup sempurna.</li>
      <hr>
      <li>Panaskan minyak banyak, goreng gulungan ayam dengan api kecil sampai kuning kecoklatan di kedua sisinya, angkat, tiriskan.</li>
      <hr>
      <li>Setelah agak hangat, potong menjadi beberapa bagian sesuai selera, sajikan dengan bahan pelengkapnya.</li>
      <hr>
</ul>

    <p><b>Catatan :</b></p>
    <ul>
      <li>Jika tidak ada plastik wrap, semat gulungan ayam dengan lidi agar tidak mudah terbuka.</li>
      <hr>
      <li>Goreng gulungan ayam dengan minyak banyak, api cenderung kecil, agar bisa matang sempurna sampai ke dalam.</li>
      <hr>
      <li>Potong ayam saat sudah tidak terlalu panas, agar keju tidak meleleh keluar.</li>
    </ul>
