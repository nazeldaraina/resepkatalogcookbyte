      <li>Lada hitam kasar, untuk taburan - secukupnya</li>
      <hr>
    </ul>
    
    <p>Langkah-langkah:</p>
    <ul>
      <li>Kupas kentang lalu potong-potong. Rebus hingga empuk sekitar 15-20 menit. Angkat dan tiriskan. Langsung haluskan kentang dengan 
