      <hr>
      <li>Dalam panci, hangatkan susu cair dan mentega. Angkat lalu tuang ke wadah kentang.</li>
      <hr>
      <li>Bumbui dengan garam dan merica. Aduk rata sambil tekan-tekan dengan saringan halus dari besi agar hasilnya lembut dan creamy.</li>
      <hr>
      <li>Siap disajikan dengan taburan lada hitam.</li>
